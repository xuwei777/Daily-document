def jsq(x,z,w):
    if z == "+":
        print("%s + %s = %s" %(x,w,x + w))
    elif z == "-":
        print("%s - %s = %s" %(x,w,x - w))
    elif z == "*":
        print("%s * %s = %s" %(x,w,x * w))
    elif z == "/":
        print("%s / %s = %s" %(x,w,x / w))
